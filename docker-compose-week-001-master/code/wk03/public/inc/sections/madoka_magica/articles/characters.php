      <p>Originally a shy second-year student, she was befriended by Madoka and eventually saved from a witch by her and Mami, who revealed that they were magical girls. When Mami died in a fight with the rampaging witch Walpurginacht, Madoka sacrificed her life to stop it. Homura, overcome by grief and uselessness, contracted with Kyubey in order to become a person that could protect Madoka, just as Madoka protected her. Her wish was to be able to return to her original meeting with Madoka, leading Homura to receive the power to manipulate time to a certain extent. Homura cares deeply for Madoka, however this caring is eventually twisted into a sole, obsessive fixation on Madoka's survival regardless of her feelings, and Homura never gives up on trying to "save" Madoka, even as she grows more cold, intimidating and antagonistic to others, taking on the role of the "rival hero" magical girl archetype.</p>
