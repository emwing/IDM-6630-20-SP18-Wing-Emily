          <p>While the Projector is being seized as evidence, Harley steals it back as part of Joker's plan and frees him, allowing him to return to Gotham with all the villains he had recruited in the Phantom Zone, including King Kong, the Gremlins, the Wicked Witch of the West, a Tyrannosaurus and a Velociraptor, Lord Voldemort, Sauron, a great white shark, the Daleks, Lord Vampyre, Agent Smith and his clones, and the Kraken. Realizing that the city does need him, Barbara releases Batman and Robin and teams up with them and Alfred to stop the Joker. Although Batman finds himself able to trust and rely on his new team upon reaching Wayne Island, he chooses to send them away rather than losing them like his parents. Upon facing him alone, Joker believes that Batman is incapable of change and zaps him to the Phantom Zone before stealing the Batcave's stash of confiscated bombs. Arriving in the Phantom Zone, Batman witnesses the harm his selfishness has caused to everyone and slowly accepts his greatest fear. Making a deal with the Zone's gatekeeper Phyllis, a talking Lego brick, to be allowed back to Gotham in order to retrieve the Zone's escaped prisoners, Batman arrives in time to save his teammates, apologizing to them for his actions, and requesting their help to stop Joker.</p>

          <p>Realizing that Joker plans to use the explosives he took to destroy the city's Energy Facility, thus causing the city to come apart and be destroyed, Batman turns Barbara into Batgirl, before he, his team, and Gotham City's other villains, who felt rejected by Joker, manage to successfully send the escaped villains back to the Phantom Zone. However, the group fails to stop Joker's bombs going off as the explosion begins to tear the city apart at the plates beneath it. Knowing this was his fault, Batman reluctantly convinces Joker that he is the true reason for being the hero he is before they, their friends and allies, and the city's inhabitants, chain-link themselves together and pull the plates back together, saving the city.</p>

